var randomNumber = Math.floor(Math.random() * wordBank.length);

var gameWord = new Word(wordBank[randomNumber]);
gameWord.createArrayOfLetters();
function guessLoop() {
  inquirer
    .prompt([
      {
        name: "guessedLetter",
        type: "input",
        message: `${gameWord.letterArray.join(" ").trim()}, ${
          gameWord.secretWord
        }`
      }
    ])
    .then(function(answer) {
      var userGuess = answer.guessedLetter.toLowerCase();
      gameWord.letterArray.forEach(function(letter) {
        if (letter.value === userGuess) {
          letter.isGuessed = true;
        }
      });
      guessLoop();
    });
}

guessLoop();
